/** Final merged dictionary: category entries + UI entries, per language. */
export const LOCAL_DICTIONARIES = Object.fromEntries(
  Object.keys(CATEGORIES).map((code) => [
    code,
    { ...CATEGORIES[code], ...(UI[code] || {}) },
  ]),
)

/** Look up an exact-match translation. Returns `null` if not curated. */
export const lookupLocal = (languageCode, sourceText) => {
  if (!languageCode || !sourceText) return null
  const dict = LOCAL_DICTIONARIES[languageCode]
  if (!dict) return null
  return Object.prototype.hasOwnProperty.call(dict, sourceText)
    ? dict[sourceText]
    : null
}

/** Diagnostic helper: how many keys are curated in each language. */
export const getDictionaryCoverage = () =>
  Object.fromEntries(
    Object.entries(LOCAL_DICTIONARIES).map(([code, dict]) => [code, Object.keys(dict).length]),
  )
